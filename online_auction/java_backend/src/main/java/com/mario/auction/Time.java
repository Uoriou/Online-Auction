package com.mario.auction;
// DTO for timer 
public class Time {

    private int time;

    public Time() {
        
    }   
    public Time(int time) {
        this.time = time;
    }       
    public int getTime() {
        return time;
    }       
    public void setTime(int time) {
        this.time = time;
    }
    
}
