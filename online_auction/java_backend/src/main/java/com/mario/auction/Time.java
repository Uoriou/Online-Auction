package com.mario.auction;
// DTO for timer 
public class Time {

    private String time;

    public Time() {
        
    }   
    public Time(String time) {
        this.time = time;
    }       
    public String getTime() {
        return time;
    }       
    public void setTime(String time) {
        this.time = time;
    }
    
}
